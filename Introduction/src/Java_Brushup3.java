
public class Java_Brushup3 {
	public static void main(String[] args) {
		// If Else for loop
		// If you have to find any one of the multiple of 2 then use /Break key in  If method .
		int[] Arr1= {52,63,85,48,598,45};
		for (int i=0; i<Arr1.length;i++)
		{
			if(Arr1[i]%2==0)
			{
				System.out.println(Arr1[i]+" is divisible by 2");
			}
			else
			{
				System.out.println(Arr1[i]+" is not divisible by 2");
			}
		}
	}
}
